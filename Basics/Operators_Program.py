num1 = float(input ("Enter a whole number between 1 and 10"))
num2 = float(input ("Enter a whole number between 1 and 10 again"))

print ("Are the numbers the same? " , num1 == num2)
print ("Are the numbers different? " , num1 != num2)
print ("The first number is bigger than the second number? " , num1 > num2)
print ("The second number is bigger than the second number? " , num2 > num1)
